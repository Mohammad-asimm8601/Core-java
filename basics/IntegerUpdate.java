/*Write a program that declares an integer variable, initializes it with a value, prints the value, updates the value, and prints the updated value. 
    output:
    original value: 5
    updated value: 8
 */
package basics;

public class IntegerUpdate {
    public static void main(String[] args) {
        int a = 5;
        System.out.println("Original value: "+a);
        a = 8;
        System.out.println("Updated value: "+a);
    }
}
