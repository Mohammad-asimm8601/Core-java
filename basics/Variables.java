package basics;

public class Variables {
    public static void main(String[] args) {
        int x = 6;
        System.out.println(x);
        x = 7;
        System.out.println(x);
        x = x + 6;
        System.out.println(x);
        x = x - 20;
        System.out.println(x);
        
    }
}
